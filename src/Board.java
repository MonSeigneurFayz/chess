
public class Board {
	private int row;
	private int col;
	
	public Board () {
		this.row = 8;
		this.col = 8;
	}
	
	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}

}
